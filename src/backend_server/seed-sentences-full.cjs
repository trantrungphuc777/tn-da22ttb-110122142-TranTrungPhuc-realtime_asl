async function seedSentences() {
    console.log('Connecting to MongoDB Atlas...');
    await mongoose.connect(MONGO_URI);
    console.log('Connected!\n');

    console.log('Deleting old sentences...');
    await ASLContent.deleteMany({ type: 'sentence' });

    const allSentences = [];
    for (const [topic, sentences] of Object.entries(SENTENCES_BY_TOPIC)) {
        for (const item of sentences) {
            allSentences.push({ type: 'sentence', label: item.s, meaning: item.m, topic, isActive: true });
        }
    }

    await ASLContent.insertMany(allSentences);

    console.log('\n====================================');
    console.log('SEED COMPLETE!');
    console.log('====================================');
    const topics = Object.keys(SENTENCES_BY_TOPIC);
    let total = 0;
    for (const t of topics) {
        const cnt = SENTENCES_BY_TOPIC[t].length;
        total += cnt;
        console.log(`  ${t}: ${cnt} sentences`);
    }
    console.log(`------------------------------------`);
    console.log(`  TOTAL: ${total} sentences / ${topics.length} topics`);
    console.log('====================================\n');

    await mongoose.disconnect();
    process.exit(0);
}

seedSentences().catch(err => { console.error('Error:', err.message); process.exit(1); });
