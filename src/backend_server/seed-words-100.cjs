// ── Hàm seed chính ──────────────────────────────────────────────────────
async function seedWords() {
    console.log('🔄 Kết nối MongoDB Atlas...');
    await mongoose.connect(MONGO_URI);
    console.log('✅ Kết nối thành công!\n');

    console.log('🗑️  Xóa toàn bộ từ vựng cũ...');
    await ASLContent.deleteMany({ type: 'word' });

    const allWords = [];
    const summary = [];
    for (const [topic, words] of Object.entries(WORDS_BY_TOPIC)) {
        for (const w of words) {
            allWords.push({ type: 'word', label: w.w, meaning: w.m, topic, isActive: true });
        }
        summary.push({ topic, count: words.length });
    }

    await ASLContent.insertMany(allWords);

    console.log('\n════════════════════════════════════════════');
    console.log('🎉 SEED TỪ VỰNG HOÀN THÀNH!');
    console.log('════════════════════════════════════════════');
    let total = 0;
    for (const { topic, count } of summary) {
        total += count;
        const flag = count < 100 ? ' ⚠️ ' : ' ✅';
        console.log(`   ${flag} ${topic.padEnd(16)}: ${count} từ`);
    }
    console.log('────────────────────────────────────────────');
    console.log(`   TỔNG: ${total} từ / ${summary.length} topics`);
    console.log('════════════════════════════════════════════\n');

    await mongoose.disconnect();
    process.exit(0);
}

seedWords().catch(err => { console.error('❌ Lỗi:', err.message); process.exit(1); });
